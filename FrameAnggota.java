    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed
           try (Connection conn = getConnection()) { // Gunakan metode getConnection
            String noAnggota = tfAnggota.getText();
            String nimText = tfNim.getText();
            String namaText = tfNama.getText();
            String alamatText = tfAlamat.getText();
            String telpText = tfTelp.getText();

            String sql = "INSERT INTO kap (no_anggota, nim, nama_anggota, alamat_anggota, no_hp) VALUES (?, ?, ?, ?, ?)";

            try (PreparedStatement pstmt = conn.prepareStatement(sql)) {
                pstmt.setString(1, noAnggota);
                pstmt.setString(2, nimText);
                pstmt.setString(3, namaText);
                pstmt.setString(4, alamatText);
                pstmt.setString(5, telpText);

                // Eksekusi query
                int rowsAffected = pstmt.executeUpdate();
                if (rowsAffected > 0) {
                    JOptionPane.showMessageDialog(this, "Data berhasil disimpan", "Sukses", JOptionPane.INFORMATION_MESSAGE);
                    clearFields();
                    tampil(); // Menampilkan data terbaru
                } else {
                    JOptionPane.showMessageDialog(this, "Data gagal disimpan", "Error", JOptionPane.ERROR_MESSAGE);
                }
            } catch (SQLException ex) {
                Logger.getLogger(FrameAnggota.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, "Gagal menyimpan data: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (SQLException ex) {
            Logger.getLogger(FrameAnggota.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Gagal melakukan koneksi: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnInsertActionPerformed

