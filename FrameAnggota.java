    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
    try (Connection conn = getConnection()) { // Gunakan metode getConnection untuk mendapatkan koneksi
        String noAnggota = tfAnggota.getText();
        if (noAnggota.isEmpty()) {
            JOptionPane.showMessageDialog(this, "No Anggota tidak boleh kosong!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        String sql = "DELETE FROM kap WHERE no_anggota = ?";

        try (PreparedStatement pstmt = conn.prepareStatement(sql)) {
            pstmt.setString(1, noAnggota); // Set no_anggota sebagai parameter untuk DELETE

            int rowsAffected = pstmt.executeUpdate();
            
            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(this, "Data berhasil dihapus", "Sukses", JOptionPane.INFORMATION_MESSAGE);
                clearFields();  // Membersihkan inputan
                tampil();        // Menampilkan data terbaru
            } else {
                JOptionPane.showMessageDialog(this, "No Anggota tidak ditemukan!", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (SQLException ex) {
            Logger.getLogger(FrameAnggota.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Gagal menghapus data: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    } catch (SQLException ex) {
        Logger.getLogger(FrameAnggota.class.getName()).log(Level.SEVERE, null, ex);
        JOptionPane.showMessageDialog(this, "Gagal melakukan koneksi: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
    try (Connection conn = getConnection()) { // Gunakan metode getConnection untuk mendapatkan koneksi
        String noAnggota = tfAnggota.getText();
        String nimText = tfNim.getText();
        String namaText = tfNama.getText();
        String alamatText = tfAlamat.getText();
        String telpText = tfTelp.getText();

        // Pastikan noAnggota tidak kosong
        if (noAnggota.isEmpty()) {
            JOptionPane.showMessageDialog(this, "No Anggota tidak boleh kosong!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Query untuk memperbarui data
        String sql = "UPDATE kap SET nim = ?, nama_anggota = ?, alamat_anggota = ?, no_hp = ? WHERE no_anggota = ?";

        try (PreparedStatement pstmt = conn.prepareStatement(sql)) {
            // Set parameter pada PreparedStatement
            pstmt.setString(1, nimText);         // NIM
            pstmt.setString(2, namaText);        // Nama Anggota
            pstmt.setString(3, alamatText);      // Alamat Anggota
            pstmt.setString(4, telpText);        // No HP
            pstmt.setString(5, noAnggota);       // No Anggota (sebagai identifier)

            // Eksekusi update
            int rowsAffected = pstmt.executeUpdate();
            
            // Menampilkan pesan berdasarkan hasil update
            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(this, "Data berhasil diperbarui", "Sukses", JOptionPane.INFORMATION_MESSAGE);
                clearFields();  // Membersihkan inputan
                tampil();        // Menampilkan data terbaru
            } else {
                JOptionPane.showMessageDialog(this, "No Anggota tidak ditemukan!", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (SQLException ex) {
            Logger.getLogger(FrameAnggota.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Gagal memperbarui data: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    } catch (SQLException ex) {
        Logger.getLogger(FrameAnggota.class.getName()).log(Level.SEVERE, null, ex);
        JOptionPane.showMessageDialog(this, "Gagal melakukan koneksi: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnUpdateActionPerformed

